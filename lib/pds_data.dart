double interpolatePDS_PS(List<double> points, double pressure) {
  final List<double> pressures = [4.2, 5.5, 6.0, 7.0];
  
  if (pressure <= 4.2) return points[0];
  if (pressure >= 7.0) {
    return (points[3] / 7.0) * pressure;
  }
  
  for (int i = 0; i < pressures.length - 1; i++) {
    if (pressure >= pressures[i] && pressure <= pressures[i + 1]) {
      double p1 = pressures[i];
      double p2 = pressures[i + 1];
      double v1 = points[i];
      double v2 = points[i + 1];
      return v1 + (v2 - v1) * (pressure - p1) / (p2 - p1);
    }
  }
  
  return points[1]; // Default to 5.5
}

double interpolatePDS(List<double> points, double pressure) {
  final List<double> pressures = [2.8, 3.5, 4.2, 5.5, 6.0, 7.0, 8.0];
  
  if (pressure <= 2.8) return points[0];
  if (pressure >= 8.0) {
    // Extrapolate
    return (points[6] / 8.0) * pressure;
  }
  
  for (int i = 0; i < pressures.length - 1; i++) {
    if (pressure >= pressures[i] && pressure <= pressures[i + 1]) {
      double p1 = pressures[i];
      double p2 = pressures[i + 1];
      double v1 = points[i];
      double v2 = points[i + 1];
      return v1 + (v2 - v1) * (pressure - p1) / (p2 - p1);
    }
  }
  
  return points[3]; // Default to 5.5
}

List<double> interpolatePS(Map<String, Map<String, List<double>>> data, double pressure) {
  final airOpen = data['air_open']!;
  final springClose = data['spring_close']!;

  double airStart = interpolatePDS_PS(airOpen['0']!, pressure);
  double airMid = interpolatePDS_PS(airOpen['55']!, pressure);
  double airEnd = interpolatePDS_PS(airOpen['90']!, pressure);

  double springStart = interpolatePDS_PS(springClose['90']!, pressure);
  double springMid = interpolatePDS_PS(springClose['35']!, pressure);
  double springEnd = interpolatePDS_PS(springClose['0']!, pressure);

  double tStart = airStart < springEnd ? airStart : springEnd;
  double tMid = airMid < springMid ? airMid : springMid;
  double tEnd = airEnd < springStart ? airEnd : springStart;

  return [tStart, tMid, tEnd];
}

